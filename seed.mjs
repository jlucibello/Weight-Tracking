function parseDate(raw) {
  const [m, d, yy] = raw.split('/').map(Number)
  const year = yy < 50 ? 2000 + yy : 1900 + yy
  return `${year}-${String(m).padStart(2, '0')}-${String(d).padStart(2, '0')}`
}

const entries = raw.trim().split('\n').map(line => {
  const [dateRaw, weightRaw] = line.split(',')
  return {
    entry_date: parseDate(dateRaw.trim()),
    weight_lbs: parseFloat(weightRaw.trim())
  }
})

console.log(`Parsed ${entries.length} entries. Inserting...`)

const CHUNK = 100
let total = 0
for (let i = 0; i < entries.length; i += CHUNK) {
  const chunk = entries.slice(i, i + CHUNK)
  const { error } = await supabase
    .from('weight_entries')
    .upsert(chunk, { onConflict: 'entry_date' })
  if (error) {
    console.error('Error inserting chunk:', error.message)
    process.exit(1)
  }
  total += chunk.length
  console.log(`  Inserted ${total}/${entries.length}...`)
}

console.log(`✅ Done! ${total} entries inserted into Supabase.`)
